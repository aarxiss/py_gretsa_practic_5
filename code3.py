# Запитуємо у користувача дані про кандидата
age = int(input("Введіть вік кандидата: "))
experience = int(input("Введіть кількість років досвіду: "))
qualification = input("Чи має кандидат спеціальну кваліфікацію (True/False)? ").strip()

# Перетворюємо введене значення кваліфікації на булеве значення
qualification = qualification == "True"

# Перевіряємо умови для відповідності кандидата вимогам
if 25 <= age <= 50 and (experience >= 3 or qualification):
    print("Кандидат відповідає вимогам")
else:
    print("Кандидат не відповідає вимогам")
